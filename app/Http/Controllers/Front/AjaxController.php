     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
        $product = Product::findOrFail($id);
        $cate_id = $product->category_id;
        $comment = Comment::where('product_id',$id)->orderBy('created_at','DESC')->paginate(10);
        $count = Comment::where('product_id',$id)->count();
        $productrecommend = Product::where('category_id','=',$cate_id)->get();
        return view('front.comment',['item'=>$product,'productrecommend'=>$productrecommend,'comment'=>$comment,'count'=>$count]);
    }

